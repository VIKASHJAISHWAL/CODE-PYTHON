
# 1.Arithmetic Operators:

# + (Addition), - (Subtraction), * (Multiplication), / (Division), % (Modulus), ** (Exponentiation), // (Floor Division).
print(10 + 5)   # Output: 15
print(10 ** 2)  # Output: 100
print(10 - 5)   # Output: 5
print(10 * 5)   # Output: 50
print(10 / 5)   # Output: 2.0
print(10 % 2)   # Output: 0
print(10 // 3)  # Output: 3
