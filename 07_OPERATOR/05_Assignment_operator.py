# 4.Assignment Operators:

# =, +=, -=, *=, /=, %=, **=, //=.
x = 10
x += 5  # Equivalent to x = x + 5
print(x)  # Output: 15
x -= 3    # Equivalent to x = x-3
print(x)  # Output: 12
x*= 2     # Equivalent to x=x*2
print(x)  # Output: 24
x/= 4     # Equivalent to x=x/4
print(x)  # Output: 6.0
x%= 4     # Equivalent to x=x%4
print(x)  # Output: 2.0
x**=3     # Equivalent to x=x**3
print(x)  # Output: 8.0
x//=2     # Equivalent to x=x//2
print(x)  # Output: 4.0
