#  2.Comparison Operators:

# == (Equal), != (Not Equal), > (Greater Than), < (Less Than), >= (Greater Than or Equal), <= (Less Than or Equal).
print(10 > 5)   # Output: True
print(10 == 5)  # Output: False
print(10 < 5)   # Output: False
print(10 >= 5)  # Output: True
print(10 <= 5)  # Output: False
